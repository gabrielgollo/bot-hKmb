// Função para decodificar Base64 e converter para código Morse
function stringToMorse(word) {
  console.log(`Word: ${word}`);
  // Converter para código Morse
  const morseString = Array.from(word).map(
    (char) => morseCode[char.toUpperCase()] || ""
  );

  return morseString;
}

